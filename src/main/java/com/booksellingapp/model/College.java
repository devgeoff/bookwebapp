package com.booksellingapp.model;

import java.io.Serializable;

@SuppressWarnings("serial")
public class College implements Serializable {
	private String clgName;
	private String clgStreet;
	private String clgCity;
	private String clgState;
	private String clgZip;
	
	public String getClgName() {
		return clgName;
	}
	public void setClgName(String clgName) {
		this.clgName = clgName;
	}
	public String getClgStreet() {
		return clgStreet;
	}
	public void setClgStreet(String clgStreet) {
		this.clgStreet = clgStreet;
	}
	public String getClgCity() {
		return clgCity;
	}
	public void setClgCity(String clgCity) {
		this.clgCity = clgCity;
	}
	public String getClgState() {
		return clgState;
	}
	public void setClgState(String clgState) {
		this.clgState = clgState;
	}
	public String getClgZip() {
		return clgZip;
	}
	public void setClgZip(String clgZip) {
		this.clgZip = clgZip;
	}
}
